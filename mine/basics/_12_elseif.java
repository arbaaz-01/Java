
public class _12_elseif {
    public static void main(String[] args) {
        int age = 20;
        if (age > 50){
            System.out.println("Hello");
        }
        else if (age > 30){
            System.out.println("hey");
        }
        else if (age > 20){
            System.out.println("see ya");
        }
        else if (age > 10){
            System.out.println("kya bolte");
        }
        else if (age > 5){
            System.out.println("kn");
        }
    }
}
