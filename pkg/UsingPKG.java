import arbaaz.*;
public class UsingPKG{
    public static void main(String[] args) {
        P p = new P();
        System.out.println(p.add(2, 3));
        System.out.println(p.sub(2, 3));
        System.out.println(p.multi(2, 3));
        System.out.println(p.div(2, 3));
        System.out.println(p.avg(2, 3));
    }
}
